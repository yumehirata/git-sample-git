package jp.co.rakus;

public class User {

	private String name;
	private Integer height;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getAge() {
		return height;
	}
	public void setAge(Integer age) {
		this.height = age;
	}
}
